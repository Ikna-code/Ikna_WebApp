/** Product structured data (schema.org/Product). */
export function getProductJsonLd(
  product: ProductSeoData,
  canonicalUrl: string,
): object {
  const price = resolvePrice(product.price);
  const primaryImage =
    product.images?.find((img) => img.is_primary)?.image_path ||
    product.image ||
    DEFAULT_OG_IMAGE;

  const allImages = product.images?.length
    ? product.images.map((img) => img.image_path).filter(Boolean)
    : [primaryImage];

  const availability =
    (product.stock ?? 1) > 0
      ? 'https://schema.org/InStock'
      : 'https://schema.org/OutOfStock';

  return {
    '@context': 'https://schema.org',
    '@type': 'Product',
    name: product.name,
    description: product.description ?? '',
    image: allImages,
    brand: {
      '@type': 'Brand',
      name: BRAND_NAME,
    },
    category:
      product.subCategory?.name ||
      product.productType?.name ||
      'Lingerie',
    offers: {
      '@type': 'Offer',
      url: canonicalUrl,
      priceCurrency: 'INR',
      price,
      availability,
      seller: {
        '@type': 'Organization',
        name: BRAND_NAME,
        url: SITE_URL,
      },
    },
  };
}

/** BreadcrumbList structured data (schema.org/BreadcrumbList). */
export function getBreadcrumbJsonLd(
  items: Array<{ name: string; url?: string }>,
): object {
  return {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: items.map((item, index) => ({
      '@type': 'ListItem',
      position: index + 1,
      name: item.name,
      ...(item.url ? { item: item.url } : {}),
    })),
  };
}

