export interface ProductSeoData {
  id: string;
  name: string;
  description?: string | null;
  price: number | string | { toNumber?: () => number };
  image?: string | null;
  colorName?: string | null;
  fabricType?: string | null;
  stock?: number;
  images?: Array<{ image_path: string; is_primary?: boolean | null }>;
  productType?: { name: string; slug?: string } | null;
  subCategory?: { name: string; slug?: string } | null;
}

function resolvePrice(price: ProductSeoData['price']): string {
  if (typeof price === 'object' && price !== null && typeof (price as any).toNumber === 'function') {
    return String((price as any).toNumber());
  }
  return String(price ?? '');
}

/** Generates rich Metadata for a product page. */
export function generateProductMetadata(product: ProductSeoData): Metadata {
  const slug = generateProductSlug(product);
  const canonical = buildCanonical(`/product/${slug}`);
  const categoryName =
    product.subCategory?.name ||
    product.productType?.name ||
    'Lingerie';

  const title = `${product.name}${product.colorName ? ` – ${product.colorName}` : ''} | IKNA`;
  const description =
    product.description
      ? `${product.description.slice(0, 140).trimEnd()}… Buy online from IKNA.`
      : `Shop ${product.name} by IKNA — premium ${categoryName.toLowerCase()} designed for all-day comfort. Free shipping across India.`;

  const ogImage = product.images?.find((img) => img.is_primary)?.image_path ||
    product.image ||
    DEFAULT_OG_IMAGE;

  return {
    title,
    description,
    keywords: [
      product.name,
      categoryName,
      BRAND_NAME,
      'buy online India',
      product.colorName ?? '',
      product.fabricType ?? '',
    ].filter(Boolean),
    alternates: { canonical },
    openGraph: {
      title,
      description,
      url: canonical,
      siteName: BRAND_NAME,
      type: 'website',
      images: [{ url: ogImage, alt: product.name }],
    },
    twitter: {
      card: 'summary_large_image',
      title,
      description,
      images: [ogImage],
    },
  };
}

/** Generates Metadata for a category (e.g. /shop/Bras). */
export function generateCategoryMetadata(
  rawCategory: string,
  subcategory?: string,
): Metadata {
  const key = rawCategory.toLowerCase();
  const copy = CATEGORY_COPY[key];

  const displayName = rawCategory.charAt(0).toUpperCase() + rawCategory.slice(1);
  const subDisplay = subcategory
    ? ` – ${subcategory.charAt(0).toUpperCase() + subcategory.slice(1)}`
    : '';

  const title = copy
    ? copy.title
    : `Buy ${displayName} Online India | ${BRAND_NAME}${subDisplay}`;

  const description = copy
    ? copy.description
    : `Shop ${displayName}${subDisplay} from IKNA — premium lingerie crafted for everyday comfort. Free shipping across India.`;

  const canonical = buildCanonical(
    subcategory
      ? `/shop/${encodeURIComponent(rawCategory)}?search=${encodeURIComponent(subcategory)}`
      : `/shop/${encodeURIComponent(rawCategory)}`,
  );

  return {
    title,
    description,
    keywords: [
      `buy ${rawCategory.toLowerCase()} online India`,
      `${rawCategory.toLowerCase()} lingerie`,
      `${rawCategory.toLowerCase()} ${BRAND_NAME.toLowerCase()}`,
      'women innerwear India',
      subcategory ?? '',
    ].filter(Boolean),
    alternates: { canonical },
    openGraph: {
      title,
      description,
      url: canonical,
      siteName: BRAND_NAME,
      type: 'website',
      images: [{ url: DEFAULT_OG_IMAGE, alt: `${displayName} – ${BRAND_NAME}` }],
    },
    twitter: {
      card: 'summary_large_image',
      title,
      description,
    },
  };
}

