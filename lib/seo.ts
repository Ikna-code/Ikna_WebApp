/** Returns a fully-qualified canonical URL for the given path. */
export function buildCanonical(path: string): string {
  const normalised = path.startsWith('/') ? path : `/${path}`;
  return `${SITE_URL}${normalised}`;
}

// ─────────────────────────────────────────────
// 3. SLUG UTILITIES
// ─────────────────────────────────────────────

function toSlugBase(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')   // strip non-alphanumeric (keep hyphens + spaces)
    .replace(/\s+/g, '-')           // spaces → hyphens
    .replace(/-+/g, '-')            // collapse multiple hyphens
    .replace(/^-+|-+$/g, '');       // trim leading/trailing hyphens
}

/**
 * Generates a human-readable, SEO-friendly product slug.
 * Format: {name-slug}-{color-slug}-{cuid}
 * Example: grace-wirefree-padded-cotton-bra-grey-cm9x7abcdefghijklmnopqr
 *
 * The cuid suffix ensures uniqueness without a DB lookup and allows
 * the server to extract the ID from the slug for fast queries.
 */
export function generateProductSlug(product: {
  name: string;
  colorName?: string | null;
  id: string;
}): string {
  const parts = [product.name, product.colorName].filter(Boolean).join(' ');
  const base = toSlugBase(parts);
  return `${base}-${product.id}`;
}

/**
 * Extracts the product ID from a slug generated by generateProductSlug.
 * Falls back to treating the entire string as an ID (backward-compat with
 * old /product/[cuid] URLs that link directly to a bare ID).
 *
 * cuid IDs are 25 lowercase alphanumeric chars (no hyphens).
 */
export function extractIdFromSlug(slug: string): string {
  const parts = slug.split('-');
  const last = parts[parts.length - 1];
  // For generated product slugs, the real ID is always the trailing segment.
  // This supports short numeric IDs, old cuid IDs, and future alphanumeric IDs.
  if (parts.length > 1 && last) {
    return last;
  }
  // Legacy: whole slug is a bare cuid
  return slug;
}

